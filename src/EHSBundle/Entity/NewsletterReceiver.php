<?php

namespace EHSBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * NewsletterReceiver
 *
 * @ORM\Table(name="newsletter_receiver")
 * @ORM\Entity(repositoryClass="EHSBundle\Repository\NewsletterReceiverRepository")
 */
class NewsletterReceiver
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="email", type="string", length=100, unique=true)
     */
    private $email;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set email
     *
     * @param string $email
     * @return NewsletterReceiver
     */
    public function setEmail($email)
    {
        $this->email = $email;

        return $this;
    }

    /**
     * Get email
     *
     * @return string 
     */
    public function getEmail()
    {
        return $this->email;
    }
}
